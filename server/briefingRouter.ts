import { z } from "zod";
import { protectedProcedure, router } from "./_core/trpc";
import { invokeLLM } from "./_core/llm";

/**
 * Pre-Market Briefing router.
 *
 * Generates a concise, educational daily briefing in Greek. The factual base —
 * the day's high/medium impact economic events — is supplied by the client
 * (sourced from the Market News feed) so the LLM only writes commentary around
 * already-known events and never invents data points. Output is markdown,
 * rendered by <Streamdown> on the client.
 */

const briefingEventSchema = z.object({
  time: z.string().max(32), // pre-formatted local time, e.g. "12:30 UTC"
  currency: z.string().max(8),
  title: z.string().max(160),
  impact: z.enum(["High", "Medium", "Low", "Holiday"]),
  forecast: z.string().max(32).optional().default(""),
  previous: z.string().max(32).optional().default(""),
});

const inputSchema = z.object({
  /** Human-readable date label, e.g. "Παρασκευή 5 Ιουνίου 2026". */
  dateLabel: z.string().max(80),
  events: z.array(briefingEventSchema).max(40),
  /** UI language; the briefing must be written in this language. Defaults to Greek. */
  lang: z.enum(["en", "el"]).optional(),
});

export const briefingRouter = router({
  /**
   * Returns a markdown pre-market briefing for the given day. Falls back to a
   * deterministic markdown skeleton if the LLM is unavailable.
   */
  generate: protectedProcedure
    .input(inputSchema)
    .mutation(async ({ input }) => {
      const lang = input.lang ?? "el";
      const fallback = buildFallback(input, lang);

      try {
        const res = await invokeLLM({
          messages: [
            {
              role: "system",
              content: lang === "en" ? SYSTEM_PROMPT_EN : SYSTEM_PROMPT,
            },
            {
              role: "user",
              content: JSON.stringify({
                date: input.dateLabel,
                events: input.events,
              }),
            },
          ],
        });

        const text = res?.choices?.[0]?.message?.content;
        const markdown =
          typeof text === "string" && text.trim().length > 0
            ? text.trim()
            : fallback;
        return {
          markdown,
          source: (markdown === fallback ? "fallback" : "llm") as
            | "llm"
            | "fallback",
          generatedAt: Date.now(),
        };
      } catch {
        return {
          markdown: fallback,
          source: "fallback" as const,
          generatedAt: Date.now(),
        };
      }
    }),
});

