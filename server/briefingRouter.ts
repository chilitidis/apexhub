  "Σου δίνονται η ημερομηνία και τα προγραμματισμένα οικονομικά events της ημέρας (impact, νόμισμα, ώρα, forecast, previous).",
  "ΜΗΝ εφεύρεις νέα οικονομικά στοιχεία ή αριθμούς — σχολίασε μόνο όσα σου δίνονται.",
  "Δομή που ΠΡΕΠΕΙ να ακολουθήσεις ακριβώς (με αυτά τα emoji & επικεφαλίδες):",
  "",
